    public EnsamblarRobotMenu(CombatSystem pSystem, MainMenu menu) {
        initComponents();
        this.pSystem = pSystem;
        this.menu = menu;
        Iterator it1 = pSystem.getPartList().iterator();
        boxCabeza.addItem("");
        boxBrazos.addItem("");
        boxTorax.addItem("");
        boxPiernas.addItem("");
        boxArma.addItem("");
        while(it1.hasNext())
        {
            Part part = (Part) it1.next();
            switch(part.getType())
            {
                case("C"):
                    boxCabeza.addItem(part.getName());
                    break;
                case("B"):
                    boxBrazos.addItem(part.getName());
                    break;
                case("T"):
                    boxTorax.addItem(part.getName());
                    break;
                case("P"):
                    boxPiernas.addItem(part.getName());
                    break;
            }
        }
        for (Gun gun : pSystem.getGunList())
        {
            boxArma.addItem(gun.getName());
        }
        
       loadTeamsPilots(pSystem);
    }
    
    private void loadTeamsPilots(CombatSystem pSystem)
    {
    	 DefaultListModel teamsModel = new DefaultListModel();
         for (String team : pSystem.getTeamsList())
         {
             teamsModel.addElement(team);
         }
         
         DefaultListModel pilotsModel = new DefaultListModel();
         for (String pilot : pSystem.getPilotsList())
         {
             pilotsModel.addElement(pilot);
         }
         listTeams.setModel(teamsModel);
         listPilotos.setModel(pilotsModel);
    }

