    private void buttonEnsamblarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEnsamblarActionPerformed
        if(!boxCabeza.getSelectedItem().equals("")&&!boxBrazos.getSelectedItem().equals("")&&!boxPiernas.getSelectedItem().equals("")&&!boxTorax.getSelectedItem().equals("")&&!boxArma.getSelectedItem().equals(""))
        {
            if(!textNombreRobot.getText().equals("")&&!textTeam.getText().equals("")&&!textNombrePiloto.getText().equals(""))
            {
                pSystem.addHumanRobot(textNombreRobot.getText(), (String)boxArma.getSelectedItem(), (String)boxPiernas.getSelectedItem(), (String)boxBrazos.getSelectedItem(), (String)boxTorax.getSelectedItem(), (String)boxCabeza.getSelectedItem(), "H", textNombrePiloto.getText(), textTeam.getText());
                textArea1.setText("Robot Ensamblado");
                boxCabeza.setSelectedIndex(0);
                boxBrazos.setSelectedIndex(0);
                boxArma.setSelectedIndex(0);
                boxPiernas.setSelectedIndex(0);
                boxTorax.setSelectedIndex(0);
                textNombreRobot.setText("");
                textNombrePiloto.setText("");
                textTeam.setText("");
                loadTeamsPilots(pSystem);
                
            }else
            {
                textArea1.setText("Nombre del robot, Equipo o Nombre del piloto no ingresado");
            }
        }else
        {
            textArea1.setText("Cabeza, Brazos, Torax, Piernas o Arma sin seleccionar");
        }
    }//GEN-LAST:event_buttonEnsamblarActionPerformed


