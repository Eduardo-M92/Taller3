    private void boxTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxTipoActionPerformed
        if(boxTipo.getSelectedItem().equals("Arma"))
        {
            labelAtaque.setText("DaÃ±o");
        }else
        {
            labelAtaque.setText("Ataque");
        }
        
        if(boxTipo.getSelectedItem().equals("C")||boxTipo.getSelectedItem().equals("P")||boxTipo.getSelectedItem().equals("T"))
        {
            textAtaque.setEditable(false);
        }
        else {
        	textAtaque.setEditable(true);
        }
        if(boxTipo.getSelectedItem().equals("B")||boxTipo.getSelectedItem().equals("P")||boxTipo.getSelectedItem().equals("Arma"))
        {
            textVida.setEditable(false);
        }
        else {
        	textVida.setEditable(true);
        }
        if(boxTipo.getSelectedItem().equals("B")||boxTipo.getSelectedItem().equals("T"))
        {
            textVelocidad.setEditable(false);
        }
        else {
        	textVelocidad.setEditable(true);
        }
    }//GEN-LAST:event_boxTipoActionPerformed

