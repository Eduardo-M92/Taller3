    private void buttonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAnadirActionPerformed

        switch(boxTipo.getItemAt(boxTipo.getSelectedIndex()))
        {
            case("C"):
                if(!textVelocidad.getText().equals("")&&!textVida.getText().equals("")&&!textNombre.getText().equals(""))
                {
                    if(pSystem.addhead("C",textNombre.getText(), boxRareza.getItemAt(boxRareza.getSelectedIndex()), Integer.parseInt(textVelocidad.getText()), Integer.parseInt(textVida.getText())))
                    {
                        textArea1.setText("Cabeza aÃ±adida");
                    }
                }else
                {
                    textArea1.setText("Campo Nombre, Vida o Velocidad vacios");
                }
                break;
            case("B"):
                if(!textNombre.getText().equals("")&&!textAtaque.getText().equals(""))
                {
                    if(pSystem.addArms("B", textNombre.getText(), boxRareza.getItemAt(boxRareza.getSelectedIndex()), Integer.parseInt(textAtaque.getText())))
                    {
                        textArea1.setText("Brazos aÃ±adidos");
                    }
                }else
                {
                    textArea1.setText("Campo Nombre o Ataque vacios");
                }
                
                break;
            case("P"):
                if(!textVelocidad.getText().equals("")&&!textNombre.getText().equals(""))
                {
                    if(pSystem.addLegs("P", textNombre.getText(), boxRareza.getItemAt(boxRareza.getSelectedIndex()), Integer.parseInt(textVelocidad.getText())))
                    {
                        textArea1.setText("Piernas aÃ±adidas");
                    }
                }else
                {
                    textArea1.setText("Campo Nombre o Velocidad vacios");
                }
                
                break;
            case("T"):
                if(!textVida.getText().equals("")&&!textNombre.getText().equals(""))
                {
                    if(pSystem.addChest("T", textNombre.getText(), boxRareza.getItemAt(boxRareza.getSelectedIndex()), Integer.parseInt(textVida.getText())))
                    {
                        textArea1.setText("Torax aÃ±adido");
                    }
                }else
                {
                    textArea1.setText("Campo Nombre o Vida vacios");
                }
                
                break;
            case("Arma"):
                if(!textVelocidad.getText().equals("")&&!textAtaque.getText().equals("")&&!textNombre.getText().equals(""))
                {
                    if(pSystem.addGun(textNombre.getText(), Integer.parseInt(textAtaque.getText()), Integer.parseInt(textVelocidad.getText())))
                    {
                        textArea1.setText("Arma aÃ±adida");
                    }
                }else
                {
                    textArea1.setText("Campo Nombre, DaÃ±o o Velocidad vacios");
                }
                break;
        }
        textNombre.setText("");
        textVelocidad.setText("");
        textVida.setText("");
        textAtaque.setText("");
        boxRareza.setSelectedIndex(0);
        boxTipo.setSelectedIndex(0);
        
    }//GEN-LAST:event_buttonAnadirActionPerformed



