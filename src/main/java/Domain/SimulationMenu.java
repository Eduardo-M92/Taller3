    private void buttonSimularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimularActionPerformed
        if(boxRobotHuman1.getSelectedItem().equals(boxRobotHuman2.getSelectedItem())||boxRobotHuman1.getSelectedItem().equals(boxRobotHuman3.getSelectedItem())||
                    boxRobotHuman2.getSelectedItem().equals(boxRobotHuman3.getSelectedItem())||boxRobotAlien1.getSelectedItem().equals(boxRobotAlien2.getSelectedItem())||
                    boxRobotAlien1.getSelectedItem().equals(boxRobotAlien3.getSelectedItem())||boxRobotAlien2.getSelectedItem().equals(boxRobotAlien3.getSelectedItem()))
        {
            textBox1.setText("No puede seleccionar el mismo robot 2 veces o 3 XD");
        }
        else
        {
        	textBox1.setText(pSystem.startSimulation((String)boxRobotHuman1.getSelectedItem(), (String)boxRobotHuman2.getSelectedItem(), (String)boxRobotHuman3.getSelectedItem(), (String)boxRobotAlien1.getSelectedItem(), (String)boxRobotAlien2.getSelectedItem(), (String)boxRobotAlien3.getSelectedItem()));
        	boxRobotAlien1.setSelectedIndex(0);
        	boxRobotAlien2.setSelectedIndex(0);
        	boxRobotAlien3.setSelectedIndex(0);
        	boxRobotHuman1.setSelectedIndex(0);
        	boxRobotHuman2.setSelectedIndex(0);
        	boxRobotHuman3.setSelectedIndex(0);
        }
    }//GEN-LAST:event_buttonSimularActionPerformed

