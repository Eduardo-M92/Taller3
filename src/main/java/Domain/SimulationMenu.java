    public SimulationMenu(CombatSystem pSystem, MainMenu menu) {
        this.pSystem = pSystem;
        this.menu = menu;
        initComponents();
        Iterator<HumanRobot> it1 = pSystem.getHumanRobotList().iterator();
        while(it1.hasNext())
        {
            HumanRobot robot =(HumanRobot) it1.next();
            DefaultListModel listHuman = new DefaultListModel();
            DefaultListModel listAlien = new DefaultListModel();
            boxRobotHuman1.addItem(robot.getName());
            boxRobotHuman2.addItem(robot.getName());
            boxRobotHuman3.addItem(robot.getName());
            String[] list = pSystem.getRobotStatisctics().split(",");
            for(int a=0;a<list.length;a++)
            {
                listHuman.addElement(list[a]);
            }
            listHumanRobots.setModel(listHuman);
            list = pSystem.getAlienStatisctics().split(",");
            for(int a=0;a<list.length;a++)
            {
                listAlien.addElement(list[a]);
            }
            listAlienRobots.setModel(listAlien);
        }
        Iterator<AlienRobot> it2 = pSystem.getAlienRobotList().iterator();
        while(it2.hasNext())
        {
            AlienRobot robot =(AlienRobot) it2.next();
            boxRobotAlien1.addItem(robot.getName());
            boxRobotAlien2.addItem(robot.getName());
            boxRobotAlien3.addItem(robot.getName());
        }
    }

