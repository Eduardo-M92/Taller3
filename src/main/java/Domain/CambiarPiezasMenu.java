    private void buttonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAceptarActionPerformed
    	HumanRobot temporalRobot= null;
        
        
        if(!boxRobot.getSelectedItem().equals(""))
        {
            for(HumanRobot robot : pSystem.getHumanRobotList())
            {
                if(robot.getName().equals(boxRobot.getSelectedItem()))
                {
                    temporalRobot = robot;
                    break;
                }
            }
            temporalRobot.setHead((String)boxCabeza.getSelectedItem());
            temporalRobot.setArms((String)boxBrazos.getSelectedItem());
            temporalRobot.setChest((String)boxTorax.getSelectedItem());
            temporalRobot.setLegs((String)boxPiernas.getSelectedItem());
            temporalRobot.setGun((String)boxArmas.getSelectedItem());
            
            textArea2.setText("Piezas cambiadas");
        }else
        {
            textArea2.setText("Seleccione un robot");
        }
    }//GEN-LAST:event_buttonAceptarActionPerformed

