    private void loadParts(CombatSystem pSystem)
    {
    	boxCabeza.removeAllItems();
    	boxBrazos.removeAllItems();
    	boxTorax.removeAllItems();
    	boxPiernas.removeAllItems();
        boxArmas.removeAllItems();
        for(Robot robot : pSystem.getHumanRobotList())
        {
            if(robot.getName().equals((String)boxRobot.getSelectedItem()))
            {
                boxCabeza.addItem(robot.getHead());
                boxBrazos.addItem(robot.getArms());
                boxTorax.addItem(robot.getChest());
                boxPiernas.addItem(robot.getLegs());
                boxArmas.addItem(robot.getGun());
                break;
            }
        }
        if(!boxRobot.getSelectedItem().equals(""))
        {
            for(Part part : pSystem.getPartList())
            {
                    if(!boxCabeza.getSelectedItem().equals(part.getName())&&!boxBrazos.getSelectedItem().equals(part.getName())&&!boxTorax.getSelectedItem().equals(part.getName())&&!boxPiernas.getSelectedItem().equals(part.getName()))
                    {
                        switch(part.getType())
                        {
                            case("C"):
                                boxCabeza.addItem(part.getName());
                                break;
                            case("B"):
                                boxBrazos.addItem(part.getName());
                                break;
                            case("T"):
                                boxTorax.addItem(part.getName());
                                break;
                            case("P"):
                                boxPiernas.addItem(part.getName());
                                break;
                        }
                    }
                }
            for(Gun gun : pSystem.getGunList())
            {
                if(!boxArmas.getSelectedItem().equals(gun.getName()))
                {
                    boxArmas.addItem(gun.getName());
                }
            }
        }
        
    }
    
